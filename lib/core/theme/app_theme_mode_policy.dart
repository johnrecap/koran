import 'package:flutter/material.dart';

abstract final class AppThemeModePolicy {
  static ThemeMode resolve(String? value) {
    switch (value) {
      case 'light':
        return ThemeMode.light;
      case 'dark':
        return ThemeMode.dark;
      case 'system':
      default:
        return ThemeMode.system;
    }
  }
}
